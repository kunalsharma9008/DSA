//Binary Search in Array
#include<stdio.h>
int main(){

int arr[5]={10,20,30,40,50};
int key=30,low=0,high=4,mid;
 while (low<=high){
	mid=(low+high)/2;
	if(arr[mid]==key){
		printf("Element %d found at position %d\n",key,mid+1);
		return 0;
	}else if(arr[mid]<key)
	low =mid+1;
	else
	high=mid-1;

	}
	printf("Element not found \n");
	return 0;
}

